/* 

OPERADORES DE ATRIBUIÇÃO

= Atribuição
+= Adição
-= Subtração
/= Divisão
*= Multiplicação
%= Resto da divisão

*/

let number = 10;

console.log((number = number + 2)); // 12
console.log((number += 2)); // 12

// Os dois exemplos acima são a mesma coisa.
// Significa que number é igual a number + 2 (ou seja, o valor é substituído)
