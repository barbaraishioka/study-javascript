// Math.PI -> 3.141592653589793
console.log(Math.PI);

// Math.pow -> Calcula a potência
// Exemplo: 2 elevado a 2 é 4
console.log(Math.pow(2, 2));

// Math.sqrt -> raiz quadrada ou outras raízes.
// Exemplo: Raiz quadrada de 25 é 5 -> Math.sqrt(25, 2)
// Exemplo: Raiz cúbica de 9 é 3 -> Math.sqrt(9, 3)
console.log(Math.sqrt(9, 3));

// Math.random -> mostra um número aleatório entre 0 e 1
console.log(Math.random());

// Math.random -> mostra um número aleatório entre 0 e 10
console.log(Math.random() * 10);

// Math.random + Math.round -> mostra um número aleatório entre 0 e 10 arredondado
console.log(Math.round(Math.random() * 10));

// Math.ceil -> arredonda um número para cima
// exemplo: 23.6 ou 23.1 vira 24
console.log(Math.ceil(23.6));

// Math.floor -> arredonda um número para baixo
// exemplo: 23.6 ou 23.1 vira 23
console.log(Math.floor(23.6));
